			    <p>The <a href="http://www.peacecorps.gov">U.S. Peace Corps</a> has been sending volunteers from the United States to countries all over the world for over 50 years. In fact, Live Lingua was founded by one of these volunteers. During his training he was amazed at how quickly and effectively the language learning material worked. Live Lingua has contacted the Peace Corps offices in Washington D.C. to obtain permission to be a repository of these courses, but we do not own any rights to them. If anybody wants to use this material for commercial purposes they will need to contact the Peace Corps offices to get permission. We are offering this material free of charge with no cost or commercials. If you have information that would lead you to believe that some of this material is not public domain, or if you have some PC training material that we have missed please contact <a href="mailto:support@livelingua.com">support@livelingua.com</a>. Enjoy the free language learning. 
